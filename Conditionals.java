public class Conditionals {
    public static void main(String[] args) {
        int a = 10;
        if (a > 10) {
            System.out.println("A is Greater than 10");
        } else if (a < 10) {
            System.out.println("A is Less Than 10");
        }

        else {
            System.out.println("A is Equal to the 10");
        }
    }
}
