public class Greatest {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a > b ? "A is Greater" : "B is Greater");
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
    }

}
