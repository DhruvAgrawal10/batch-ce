public class Switch {
    public static void main(String[] args) {
        int a = 2;
        switch (a) {
            case 1:
                System.out.println("You have Choose 1");
                break;

            case 2:
                System.out.println("You have Choose 2");
                break;
            default:
                System.out.println("Choose the Correct Option");
                break;
        }
    }

}
